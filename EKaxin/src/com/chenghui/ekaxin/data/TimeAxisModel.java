package com.chenghui.ekaxin.data;

public class TimeAxisModel {
	
	private int imgtitle;
	private int imgpicture;
	private String content;
	private String date;
	
	public int getImgTitle() {
		return imgtitle;
	}
	public void setImgTitle(int imgHead) {
		this.imgtitle = imgHead;
	}
	
	public int getImgPicture() {
		return imgpicture;
	}
	public void setImgPicture(int imgHead) {
		this.imgpicture = imgHead;
	}
	
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
}
